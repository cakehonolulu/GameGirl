/*
	BIT 7, H
	Opcode: 0x7C
	Number of Bytes: 2
	Number of Cycles: 2

	Copy the complement of the contents of bit 7 in register H
	to the Z flag of the program status word (PSW).
*/
void m_bit_7_h()
{
	if (!m_is_bit_set(H, Z))
	{
		FLAG_SET(Z);
	} else {
		FLAG_UNSET(Z);
	}

	FLAG_UNSET(N);

	FLAG_SET(H);
#ifdef OPCODE_DEBUG
	printf("Flags: 0x%02X\n", FLAGS);
#endif
	PC += 2;
}
